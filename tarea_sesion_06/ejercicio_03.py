#Suma de numeros 7 y 3
numero1 = 7
numero2 = 3

# Calcular la suma
suma = numero1 + numero2

# Verificar si la suma es par
if suma % 2 == 0:
    print("La suma de 7 y 3 es un número par.")
else:
    print("La suma de 7 y 3 no es un número par.")

