# 1. Imprimir los 20 primeros numeros de la serie de Fibonacci 

# Inicializar los primeros dos números de la serie
a, b = 0, 1

# Imprimir los primeros 20 números de la serie de Fibonacci
for _ in range(20):
    print(a)
    # Calcular el siguiente número de Fibonacci
    a, b = b, a + b

















