# 6. De las tuplas de coordenadas (x1,y1) = (40,80) ; (x2,y2) = (50,50)
# Encuentra las coordenadas del punto medio, almacena en una tupla e imprime el resultado (x,y)

x1, y1 = 40, 80
x2, y2 = 50, 50

x_medio = (x1 + x2) / 2
y_medio = (y1 + y2) / 2

punto_medio = (x_medio, y_medio)

print("El punto medio es:", punto_medio)
